export class Empleado {

    nombre: String;
    apellidos: String;
    dni: String;
    departamento: String;
    email: String;
    imagen: String;

    constructor(nombre: String = "", apellidos: String = "", dni: String = "", departamento: String = "", email: String = "", imagen: String = "") {

        this.nombre = nombre;
        this.apellidos = apellidos;
        this.dni = dni;
        this.departamento = departamento;
        this.email = email;
        this.imagen = imagen

    }




}